class ProgressBar(Flowable):
    """Flowable that draws a rounded horizontal progress bar."""
    def __init__(self, width, height, progress, color, bg_color=colors.HexColor("#F1F5F9")):
        super().__init__()
        self.width = width
        self.height = height
        self.progress = min(max(float(progress), 0.0), 1.0)
        self.color = color
        self.bg_color = bg_color

    def wrap(self, availWidth, availHeight):
        return self.width, self.height

    def draw(self):
        self.canv.saveState()
        # Track background
        self.canv.setFillColor(self.bg_color)
        self.canv.setStrokeColor(colors.transparent)
        self.canv.roundRect(0, 0, self.width, self.height, self.height / 2, fill=1, stroke=0)
        
        # Progress fill
        if self.progress > 0:
            self.canv.setFillColor(self.color)
            fill_width = self.width * self.progress
            self.canv.roundRect(0, 0, fill_width, self.height, self.height / 2, fill=1, stroke=0)
            
        self.canv.restoreState()


class CircleGauge(Flowable):
    """Flowable that draws a circular gauge with score in center."""
    def __init__(self, size, score):
        super().__init__()
        self.size = size
        self.score = score

    def wrap(self, availWidth, availHeight):
        return self.size, self.size

    def draw(self):
        cx = self.size / 2
        cy = self.size / 2
        r = self.size / 2 - 6
        
        self.canv.saveState()
        
        # 1. Background track
        self.canv.setStrokeColor(colors.HexColor("#F1F5F9"))
        self.canv.setLineWidth(8)
        self.canv.setFillColor(colors.transparent)
        self.canv.circle(cx, cy, r, fill=0, stroke=1)
        
        # 2. Progress arc
        if self.score >= 75:
            color = colors.HexColor("#0f766e")
        elif self.score >= 50:
            color = colors.HexColor("#d97706")
        else:
            color = colors.HexColor("#dc2626")
            
        self.canv.setStrokeColor(color)
        self.canv.setLineCap(1) # Rounded cap
        self.canv.setLineWidth(8)
        
        x1, y1 = cx - r, cy - r
        x2, y2 = cx + r, cy + r
        extent = -360 * (self.score / 100.0)
        if abs(extent) > 0.001:
            self.canv.arc(x1, y1, x2, y2, 90, extent)
        
        self.canv.restoreState()
        
        # 3. Score text
        self.canv.saveState()
        self.canv.setFont("Helvetica-Bold", 24)
        self.canv.setFillColor(colors.HexColor("#1E293B"))
        self.canv.drawCentredString(cx, cy - 4, f"{self.score}")
        
        self.canv.setFont("Helvetica-Bold", 8)
        self.canv.setFillColor(colors.HexColor("#64748B"))
        self.canv.drawCentredString(cx, cy - 16, "/ 100")
        self.canv.restoreState()


class DomainDonutChart(Flowable):
    """Flowable that draws a 5-domain ring donut."""
    def __init__(self, size):
        super().__init__()
        self.size = size
        self.colors = [
            colors.HexColor("#2563EB"), # Attention (Blue)
            colors.HexColor("#7C3AED"), # Memory (Purple)
            colors.HexColor("#0D9488"), # Reasoning (Teal)
            colors.HexColor("#10B981"), # Coordination (Green)
            colors.HexColor("#D85A30"), # Perception (Orange/Red)
        ]

    def wrap(self, availWidth, availHeight):
        return self.size, self.size

    def draw(self):
        cx = self.size / 2
        cy = self.size / 2
        r = self.size / 2 - 8
        
        self.canv.saveState()
        self.canv.setLineWidth(14)
        self.canv.setLineCap(0) # Butt caps
        
        x1, y1 = cx - r, cy - r
        x2, y2 = cx + r, cy + r
        
        # 5 equal segments of 72 degrees each, with spacing
        start_angle = 90
        extent = -72
        
        for col in self.colors:
            self.canv.setStrokeColor(col)
            # 4 degrees gap by drawing -68 extent and skipping 4 degrees
            self.canv.arc(x1, y1, x2, y2, start_angle - 2, extent + 4)
            start_angle += extent
            
        self.canv.restoreState()
        
        self.canv.saveState()
        self.canv.setFont("Helvetica-Bold", 18)
        self.canv.setFillColor(colors.HexColor("#1E293B"))
        self.canv.drawCentredString(cx, cy - 3, "5")
        
        self.canv.setFont("Helvetica-Bold", 7)
        self.canv.setFillColor(colors.HexColor("#64748B"))
        self.canv.drawCentredString(cx, cy - 13, "DOMAINS")
        self.canv.restoreState()


class CardIcon(Flowable):
    """Flowable that draws custom task icons."""
    def __init__(self, size, color_hex, icon_type="circle"):
        super().__init__()
        self.size = size
        self.color_hex = color_hex
        self.icon_type = icon_type

    def wrap(self, availWidth, availHeight):
        return self.size, self.size

    def draw(self):
        self.canv.saveState()
        
        # Map primary hex to light bg hex
        bg_colors = {
            "#3b82f6": "#EFF6FF", # CPT Blue
            "#ef4444": "#FEF2F2", # Go/No-Go Red
            "#6366f1": "#F5F3FF", # N-Back Indigo
            "#10b981": "#ECFDF5", # Word Recall Emerald / Shape Match Green
            "#f59e0b": "#FEF3C7", # Updating Amber
            "#14b8a6": "#F0FDFA", # Tower Teal
            "#8b5cf6": "#F5F3FF", # Divided Attention Purple
        }
        
        fill_color = colors.HexColor(bg_colors.get(self.color_hex.lower(), "#F1F5F9"))
        self.canv.setFillColor(fill_color)
        self.canv.setStrokeColor(colors.transparent)
        self.canv.roundRect(0, 0, self.size, self.size, 4, fill=1, stroke=0)
        
        # Draw the symbol
        self.canv.setFillColor(colors.HexColor(self.color_hex))
        self.canv.setStrokeColor(colors.HexColor(self.color_hex))
        self.canv.setLineWidth(1.2)
        self.canv.setLineCap(1)
        
        cx = self.size / 2
        cy = self.size / 2
        
        if self.icon_type == "activity":
            p = self.canv.beginPath()
            p.moveTo(cx - 5, cy)
            p.lineTo(cx - 2.5, cy)
            p.lineTo(cx - 1.25, cy + 4)
            p.lineTo(cx + 1.25, cy - 4)
            p.lineTo(cx + 2.5, cy)
            p.lineTo(cx + 5, cy)
