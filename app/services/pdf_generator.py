class RoundedTable(Table):
    """Subclass of Table that draws a rounded background and border."""
    def __init__(self, data, colWidths=None, rowHeights=None, style=None, 
                 bgColor=None, borderColor=None, borderRadius=8, borderWidth=0.5, **kwargs):
        super().__init__(data, colWidths, rowHeights, style, **kwargs)
        self.bgColor = bgColor
        self.borderColor = borderColor
        self.borderRadius = borderRadius
        self.borderWidth = borderWidth

    def draw(self):
        self.canv.saveState()
        if self.bgColor:
            self.canv.setFillColor(self.bgColor)
        else:
            self.canv.setFillColor(colors.white)
            
        if self.borderColor:
            self.canv.setStrokeColor(self.borderColor)
            self.canv.setLineWidth(self.borderWidth)
        else:
            self.canv.setStrokeColor(colors.transparent)
            
        self.canv.roundRect(0, 0, self._width, self._height, self.borderRadius, 
                             fill=1 if self.bgColor else 0, 
                             stroke=1 if self.borderColor else 0)
        self.canv.restoreState()
        super().draw()


